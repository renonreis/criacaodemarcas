          <b>d) O mundo da tipografia (falando entre as linhas).</b><br/>
          Lembre-se: a escolha da tipografia da marca deve impactar seu público-alvo, não massagear seu ego e preferências pessoais. Agora que este ponto já está devidamente esclarecido, o profissional de design irá avaliar a composição de fontes que irá se comunicar com seus potenciais clientes de forma assertiva.
        </p>
        <p>
          Para ser efetivo, a tipografia requer um estudo aprofundado do negócio e seus objetivos (olha aí o propósito da marca novamente!). Os tipos precisam ser adequadamente trabalhados para transmitir a mensagem da sua empresa com exatidão, além de terem o equilíbrio necessário com os demais elementos gráficos e boa legibilidade.
        </p>
        <p>
          Alguns conceitos básicos que precisam ser levados em consideração na hora da escolha da tipografia para <b>criação de marca e logotipo</b> são:
        </p>
        <p>
          <em>Classificação das Fontes:</em> existem milhares de fontes por aí, porém todas elas se encaixam em seis categorias básicas: estilo antigo, moderno, serifa grossa, sem serifa, manuscrito e decorativo.
        </p>
        <p>
          A primeira categoria, o <span class="antigo">estilo antigo</span> é a fonte perfeita para um texto longo, onde você precisa que uma audiência preste atenção no que está sendo dito por bastante tempo. Livros costumam usar fontes neste formato. Por ser um estilo desenvolvido há séculos e pela escrita à mão com ajuda de uma pena, é uma fonte que possui este aspecto de “invisibilidade”. Não chama muita atenção, mas prende muita atenção para leitura. É uma fonte que sempre possui serifa (estes pequenos detalhes ao na extremidade das letras) e tem uma transição do grosso ao fino nos seus traços.
        </p>
        <p>
          Em seguida, com o <span class="moderno">estilo moderno</span> nos deparamos com um estilo mais “mecânico” de fonte, mais frio e elegante. Por possuir uma aparência muito forte, não é o estilo indicado para um texto corrido, pois pode torná-lo cansativo. É a fonte indicada se você procura passar uma mensagem rápida ou criar um título marcante. É ideal usá-la em um tamanho grande.
        </p>
        <p>
          O estilo de <span class="moderno">serifa grossa</span>, assim como o estilo antigo, é ótimo para textos extensos, por conta de seus traços grossos. O peso de cada letra deste estilo é igual, então é um bom estilo para se usar em pôsteres e peças de propaganda com maior seriedade.
        </p>
        <p>
          Do outro lado do espectro temos a fonte sem serifa, que não possui o famoso detalhe da serifa nas extremidades das letras. As letras neste estilo tem todas a mesma espessura, sem a transição do grosso-fino, e portanto elas sempre têm o mesmo peso. Esse estilo de fontes costuma ter uma grande variedade entre o "light" (ou suave) e o "bold" (negrito mais forte). É um estilo que pode te garantir uma grande diversidade, ideais para chamadas e para ser usada em meios digitais. 
        </p>
        <p>
          A fonte de <span class="manuscrito">estilo manuscrito</span>, que de uns anos para cá se tornou febre entre entusiastas de lettering ao redor do mundo, é nada menos que qualquer estilo que parece ter sido escrito à mão, com pincel, caneta tinteiro ou uma caneta profissional. Existem inúmeras subdivisões dentro desta categoria -manuscritos que se conectam, manuscritos que não se conectam, manuscritos que imitam calígrafos tradicionais e assim por diante. De toda forma, no geral, por ser uma categoria de fontes mais elaboradas, é ideal para ser usada em tamanho grande em assinaturas ou marcas com muita personalidade.
        </p>
        <p>
          Por fim, temos o <span class="decorativo">estilo decorativo</span>. Casual e divertido, é a fonte ideal para títulos e chamadas extremamente personalizados. Dependendo da forma que você decidir usá-la, poderá invocar infinitas emoções, das mais óbvias e claras até as mais enigmáticas e cheias de ambiguidades. Existe uma variedade de fontes no estilo decorativo, mas no geral é uma fonte usada para transmitir muita personalidade. Por isso, explore qual fonte será usada com propósito e faça uso limitado e consciente deste estilo aqui.
        </p>
        <p>
          Cada família de fontes ainda pode ter suas variações quanto a estilo (Thin, Light, Regular, Medium, Bold, Black e Extra Bold) e larguras (Condensado, Regular e Estendido). É muito importante não mudar as características da fonte em um editor de imagens, pois perde-se a estrutura da fonte e inviabiliza seu uso em outros lugares. Opte sempre por fontes que já possuem a variação desejada na <b>identidade visual da marca.</b>
        </p>
        <p>
          <em>Medidas Tipográficas:</em> este é um conceito que acompanha as fontes tipográficas e diz respeito às linhas de referência que cada caractere possui e delimitam o espaço que podem ocupar. Cada família de tipos tem a sua e elas são conhecidas como: ascendente, linha de caixa alta, linha de base e descendente.
        </p>
        <p>
          <ul style="padding-left: 35px;">
            <li>
              <b>Ascendente:</b> linha que acompanha a altura dos caracteres <b>b, d, f, h, l, t</b>, ou seja, é a altura máxima do corpo da fonte.
            </li>
            <li>
              <b>Linha de Caixa Alta:</b> altura correspondente às letras em caixa alta (<b>S, B, M</b>).
            </li>
            <li>
              <b>Linha de Base:</b> a linha na qual a maioria dos tipo tem como base (com exceção dos tipos descendentes).
            </li>
            <li>
              <b>Descendentes:</b> linha que delimita o quão baixo um tipo pode chegam em relação a linha de base como o <b>g, j, ç</b>.
            </li>
          </ul>
